Cypress.Commands.add('login', (index) => {
    cy.fixture("login-data.json").then((loginData) => {
        cy.contains('Log in').click();
        cy.get('#username').type(loginData.users[index].username);
        cy.get('#password').type(loginData.users[index].password);
        cy.get('#login').click();
    })
})
